#int casting
a = int(1)   # a will be 1
b = int(2.8) # b will be 2
c = int("3") # c will be 3

#float casting
d = float(1)     # d will be 1.0
e = float(2.8)   # e will be 2.8
f = float("4.2") # f will be 4.2
g = float("3")   # g will be 3.0

#string casting
h = str("s1") # h will be 's1'
i = str(2)    # i will be '2'
j = str(3.0)  # j will be '3.0'